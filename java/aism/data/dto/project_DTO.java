package aism.data.dto;

public class project_DTO {

	
	private String date_time; 
	private String project_name; 
	private String track_num ;
	private String song_name ;
	private String origin_song; 
	private String ai_song ;
	private String result_song;
	
	public String getDate_time() {
		return date_time;
	}
	public void setDate_time(String date_time) {
		this.date_time = date_time;
	}
	public String getProject_name() {
		return project_name;
	}
	public void setProject_name(String project_name) {
		this.project_name = project_name;
	}
	public String getTrack_num() {
		return track_num;
	}
	public void setTrack_num(String track_num) {
		this.track_num = track_num;
	}
	public String getSong_name() {
		return song_name;
	}
	public void setSong_name(String song_name) {
		this.song_name = song_name;
	}
	public String getOrigin_song() {
		return origin_song;
	}
	public void setOrigin_song(String origin_song) {
		this.origin_song = origin_song;
	}
	public String getAi_song() {
		return ai_song;
	}
	public void setAi_song(String ai_song) {
		this.ai_song = ai_song;
	}
	public String getResult_song() {
		return result_song;
	}
	public void setResult_song(String result_song) {
		this.result_song = result_song;
	}
	
	
	
	
	
	
}
